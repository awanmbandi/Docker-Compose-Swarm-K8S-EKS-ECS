###########################################################
################ DOCKER COMPOSE COMMANDS ##################
###########################################################
### Create and start containers
docker-compose up

### Stop and remove containers, networks, images, and volumes
docker-compose down

### Build or rebuild services
docker-compose build

### View output from containers
docker-compose logs

### List containers
docker-compose ps

### Restart services
docker-compose restart

### Start services
docker-compose start

### Stop services
docker-compose stop

### Pull service images
docker-compose pull

### Push service images
docker-compose push